@main
struct MultipleVariables {
    static func main() {
        var x = 1, y = 2, z = 3
        let a = 10, b = 20
        print(x, y, z, a, b)

        var i: Int = 1, j: Int = 2
        let firstName: String = "Robin", lastName: String = "Refsnes"
        print(i + j, firstName, lastName)
    }
}
