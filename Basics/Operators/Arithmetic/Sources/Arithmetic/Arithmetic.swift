@main
struct Arithmetic {
    static func main() {
        let a = 7, b = 3
        print(a - b)
        print(a * b)
        print(a / b)        // integer division

        print(7 % 3)   // 1
        print(8 % 2)   // 0
        print(10 % 6)  // 4
    }
}
